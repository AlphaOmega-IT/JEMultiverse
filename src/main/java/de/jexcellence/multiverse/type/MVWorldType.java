package de.jexcellence.multiverse.type;

/**
 * Enumerates the different types of worlds supported by this plugin.
 */
public enum MVWorldType {
  /**
   * A standard, default-type world.
   */
  DEFAULT,

  /**
   * A void world containing no landscape.
   */
  VOID,

  /**
   * A world designed for plot-based structures.
   */
  PLOT
}